    private void bntSelecionarArquivoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bntSelecionarArquivoActionPerformed
        // TODO add your handling code here:
        JFileChooser fileChooser = new JFileChooser();
    fileChooser.setDialogTitle("Selecione um arquivo");

    int resultado = fileChooser.showOpenDialog(this);

    if (resultado == JFileChooser.APPROVE_OPTION) {
        File arquivoSelecionado = fileChooser.getSelectedFile();
        txtCaminhoArquivo.setText(arquivoSelecionado.getAbsolutePath());  
    }
    }//GEN-LAST:event_bntSelecionarArquivoActionPerformed

