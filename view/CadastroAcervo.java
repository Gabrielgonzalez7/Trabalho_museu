        // TODO add your handling code here:
        String tipo = cmbTipo.getSelectedItem().toString();
        String titulo = txtTitulo.getText();
        String descricao = txtDescricao.getText();
        String data = txtData.getText();
        String tags = txtTags.getText();
        String local = txtLocal.getText();
        String doador = txtDoador.getText();
        String arquivo = txtArquivo.getText();

        // Validação simples
        if (titulo.isEmpty()) {
            JOptionPane.showMessageDialog(this, "O título é obrigatório!");
            return;
        }

        // Aqui você envia ao DAO para salvar no banco...
        JOptionPane.showMessageDialog(this, 
            "Item adicionado com sucesso!\n(Tipo: " + tipo + ")");
        
