    private void bntRegistrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bntRegistrarActionPerformed
        // TODO add your handling code here:
        String idUsuario = txtIdUsuario.getText().trim();
    String idMaterial = txtIdMaterial.getText().trim();
    String dataEmp = txtDataEmprestimo.getText().trim();
    String dataPrev = txtDataPrevista.getText().trim();
    String obs = txtObs.getText().trim();

    if (idUsuario.isEmpty() || idMaterial.isEmpty()) {
        JOptionPane.showMessageDialog(this, "Informe usuário e material.");
        return;
    }

    String sql = "INSERT INTO Emprestimo (idUsuario, idMaterial, dataEmprestimo, dataPrevista, observacoes) VALUES (?, ?, ?, ?, ?)";
    try (Connection conn = Conexao.getConnection();
         PreparedStatement ps = conn.prepareStatement(sql)) {

        ps.setInt(1, Integer.parseInt(idUsuario));
        ps.setInt(2, Integer.parseInt(idMaterial));
        ps.setString(3, dataEmp);
        ps.setString(4, dataPrev);
        ps.setString(5, obs);

        int n = ps.executeUpdate();
        if (n > 0) {
            JOptionPane.showMessageDialog(this, "Empréstimo registrado com sucesso!");
            this.dispose(); // fecha a tela
        } else {
            JOptionPane.showMessageDialog(this, "Erro ao registrar empréstimo.");
        }
    } catch (Exception e) {
        e.printStackTrace();
        JOptionPane.showMessageDialog(this, "Erro: " + e.getMessage());
    }
    }//GEN-LAST:event_bntRegistrarActionPerformed

