    private void bntBuscarUsuarioActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bntBuscarUsuarioActionPerformed
        // TODO add your handling code here:
        String idStr = txtIdUsuario.getText().trim();
    if (idStr.isEmpty()) {
        JOptionPane.showMessageDialog(this, "Digite o ID do usuário.");
        return;
    }
    try (Connection conn = Conexao.getConnection()) {
        String sql = "SELECT nome, tipo FROM Usuario WHERE id = ?";
        PreparedStatement st = conn.prepareStatement(sql);
        st.setInt(1, Integer.parseInt(idStr));
        ResultSet rs = st.executeQuery();
        if (rs.next()) {
            lblNomeUsuario.setText("Nome: " + rs.getString("nome"));
            lblTipoUsuario.setText("Tipo: " + rs.getString("tipo"));
        } else {
            JOptionPane.showMessageDialog(this, "Usuário não encontrado.");
            lblNomeUsuario.setText("Nome:");
            lblTipoUsuario.setText("Tipo:");
        }
    } catch (Exception e) {
        e.printStackTrace();
        JOptionPane.showMessageDialog(this, "Erro ao buscar usuário: " + e.getMessage());
    }
    }//GEN-LAST:event_bntBuscarUsuarioActionPerformed

