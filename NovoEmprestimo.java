    private void bntBuscarMaterialActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bntBuscarMaterialActionPerformed
        // TODO add your handling code here:
        String idStr = txtIdMaterial.getText().trim();
    if (idStr.isEmpty()) {
        JOptionPane.showMessageDialog(this, "Digite o ID do material.");
        return;
    }
    try (Connection conn = Conexao.getConnection()) {
        String sql = "SELECT titulo, quantidade FROM Material WHERE id = ?";
        PreparedStatement st = conn.prepareStatement(sql);
        st.setInt(1, Integer.parseInt(idStr));
        ResultSet rs = st.executeQuery();
        if (rs.next()) {
            lblTituloMaterial.setText("Título: " + rs.getString("titulo"));
            int qtd = rs.getInt("quantidade");
            lblDisponivel.setText(qtd > 0 ? "Disponível: Sim (" + qtd + ")" : "Disponível: Não (0)");
        } else {
            JOptionPane.showMessageDialog(this, "Material não encontrado.");
            lblTituloMaterial.setText("Título:");
            lblDisponivel.setText("Disponível:");
        }
    } catch (Exception e) {
        e.printStackTrace();
        JOptionPane.showMessageDialog(this, "Erro ao buscar material: " + e.getMessage());
    }
    }//GEN-LAST:event_bntBuscarMaterialActionPerformed

